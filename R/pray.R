#' @title Display Shinto Shrine ASCII Art
#'
#' @description The `shinto()` function outputs a simple ASCII art representation of a Shinto shrine to the console.
#'
#' @return NULL
#' @export
#'
#' @examples
#' shinto()
shinto <- function() {
  cat("   \n")
  cat("  __________________________________________________________\n")
  cat("  \\________________________________________________________/ \n")
  cat("       |      |             |    |             |      | \n")
  cat("     __|      |_____________|____|____________ |      |__ \n")
  cat("  . |__|      |________________________________|      |__|\n")
  cat("       |      |   _______//  /_||_\\  \\______   |      |\n")
  cat("       |      |  /::::://  /|| || ||\\  \\:::::\\ |      |\n")
  cat("       |      |//:::://   ~~~~~~~~~~~   \\:::::\\|      |\n")
  cat("      /|      | ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~ |      |\\ \n")
  cat("    /::|      | ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~ |      |::\\ \n")
  cat("   ~~~ |      || ~~~~~~~~~~~~~(P)~~~~~~~~~~~~ ||      |~~~\n")
  cat("       |      ||      *      / S \\      *    .||      |\n")
  cat("       |      || __________/   S   \\_________ ||      |\n")
  cat("       |      || :::||+++             +++|::: ||      |\n")
  cat("       |      || :::||+++ ___________ +++|::: ||      |\n")
  cat("       |      || :::||++  |*********|  ++|::: ||      |\n")
  cat("      |~~~~~~~~~|     ==================     |~~~~~~~~~| \n")
  cat("  ::::::::::::::::::======================:::::::::::::::::::\n")
  cat("  :::::::::::::::::/                      \\::::::::::::::::::\n")
  cat("  ::::::::::::::::/                        \\:::::::::::::::::\n")
  cat("  :::::::::::::::/                          \\::::::::::::::::\n")
  cat("   \n")
  cat("   \n")
}
